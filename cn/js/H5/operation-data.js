function edit(year) {
  var total = parseFloat(0);
  var list = "";
  for (var i = 0; i < year.length; i++) {
    list +=
      "<li><span>" +
      (i + 1) +
      "月</span><span></span><span>" +
      year[i] +
      "</span></li>\n";
    total += parseFloat(year[i].replace(/,/g, ""));
  }
  var category_ed = $("#data_category").find(":selected").val();
  // console.log(category_ed);
  var total_rounding = total.toFixed(1);
  var total_str = total_rounding.toString();
  var total_arr = total_str.split(".");
  var total_end =
    total_arr[0].replace(/(\d)(?=(\d{3})+$)/g, "$1,") + "." + total_arr[1];
  // console.log('total_end', total_end)
  $(".big-size span").eq(2).empty().append(total_end);
  $(".table-con ul").empty().append(list);
  $(".table-con ul li").each(function () {
    var span_txt = $(this).children("span").eq(2).text();
    var span_num = span_txt.replace(/,/g, "");
    var span_scale = "";
    var op_num = $("option:selected").val();
    // console.log(op_num);
    if (
      op_num == 2023 ||
      op_num == 2022 ||
      op_num == 2021 ||
      op_num == 2020 ||
      op_num == 2019 ||
      op_num == 2018 ||
      op_num == 2017 ||
      op_num == 2016 ||
      op_num == 2015 ||
      op_num == 2014 ||
      op_num == 2013 ||
      op_num == 2012 ||
      op_num == 2011 ||
      op_num == 2010 ||
      op_num == 2009 ||
      category_ed == "xin"
    ) {
      span_scale = span_num / 40;
    }
    if (category_ed == "jiao") {
      span_scale = span_num / 10000;
    } else if (category_ed == "ti") {
      span_scale = span_num / 7500;
    } else if (category_ed == "ling") {
      span_scale = span_num / 1000;
    }
    $(this).children("span").eq(1).css("height", span_scale);
  });
}
